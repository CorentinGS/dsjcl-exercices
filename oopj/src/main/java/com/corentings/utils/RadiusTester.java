package com.corentings.utils;

public class RadiusTester {
    double input;
    double expected;

    public RadiusTester(double input, double expected) {
        this.input = input;
        this.expected = expected;
    }

    public double getInput() {
        return input;
    }

    public double getExpected() {
        return expected;
    }
}
